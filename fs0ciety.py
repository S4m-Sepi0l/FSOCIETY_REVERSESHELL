def send_command(client, cmd):
    client.send(cmd.encode('utf-8', errors='ignore'))

def receive_response(client, timeout=2.0):
    client.settimeout(timeout)
    response = b""
    try:
        while True:
            chunk = client.recv(4096)
            if not chunk:
                break
            response += chunk
            if len(chunk) < 4096:
                break
    except socket.timeout:
        pass
    except Exception:
        pass
    
    return response.decode('cp437', errors='ignore') if response else ""

